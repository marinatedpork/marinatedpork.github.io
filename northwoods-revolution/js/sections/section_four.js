$(document).ready(function(){

	var findCurrent = function(videos) {
			var video;
			videos.forEach(function(obj, index, videos){
				if (obj.playing === true) {
					video = obj;
				}
			});
			return video;
		};

		var parseVideos = function(videos) {
			videos.forEach(function(obj, index) {
				$("ul[data-category='"+obj.category+"']").append("<li data-video="+index+"><span>"+obj.title+"</span></li>");
			});
		};

		var changeVideo = function(nextVideo, videos) {
			videos[findCurrent(videos).index].playing = false;
			videos[nextVideo.index].playing = true;
			$("#video-frame").fadeOut(300, function(){
				$("#video-frame").html(nextVideo.src);
				$("#video-frame iframe").load(function(){
					$("#video-frame").fadeIn(300, function(){
						$('.portfolio-info-row').children(':not(.portfolio-description)').fadeIn(400, function(){
							$('.portfolio-description').fadeIn(400);
						});
					});
				});
			});
			$(".portfolio-description").html(nextVideo.description);
			$(".portfolio-client").html(nextVideo.client.toUpperCase());
			$(".portfolio-title").html(nextVideo.title);
			$("li[data-video].active").removeClass("active");
			$("li[data-video='"+nextVideo.index+"']").addClass("active");
		};

		parseVideos(videos);

		videos[0].playing = true;
		$(".portfolio-controls").hide();
		$("#controls-description").text(videos[0].description);
		$("body").on("click", "li[data-video]", function(event){
			var nextVideo = videos[parseInt($(event.target).closest("li[data-video]").attr("data-video"))];
			$('.portfolio-info-row').children().fadeOut(400, function(){
				changeVideo(nextVideo, videos);
			});
		});

});

	